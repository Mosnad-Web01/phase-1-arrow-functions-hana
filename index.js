// دالة للتقسيم بين عددين
const divide = function(a, b) {
    return a / b;
  };
  
  // دالة سهمية تأخذ معاملًا واحدًا وترفعه للقوة الثانية
  const square = x => x * x;
  
  // دالة سهمية تجمع بين معاملين
  const add = (a, b) => a + b;

  console.log(divide(10,3))  ;
  console.log(square(10,3))  ;
  console.log(add(10,3))  ;
